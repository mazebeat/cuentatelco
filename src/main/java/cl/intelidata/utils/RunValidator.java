package cl.intelidata.utils;

import javax.faces.application.FacesMessage;
import javax.faces.component.UIComponent;
import javax.faces.context.FacesContext;
import javax.faces.validator.FacesValidator;
import javax.faces.validator.Validator;
import javax.faces.validator.ValidatorException;

/**
 *
 * @author DFeliu
 */
@FacesValidator("rutValidator")
public class RunValidator implements Validator {

    @Override
    public void validate(FacesContext context, UIComponent component, Object value) throws ValidatorException {

        String rut = value.toString().replace(".", "").replace("-", "");
        int rutAux = Integer.parseInt(rut.substring(0, rut.length() - 1)), m = 0, s = 1;
        char dv = rut.charAt(rut.length() - 1);

        for (; rutAux != 0; rutAux /= 10) {
            s = (s + rutAux % 10 * (9 - m++ % 6)) % 11;
        }
        if (dv == (char) (s != 0 ? s + 47 : 75)) {
            return;
        }

        throw new ValidatorException(new FacesMessage("RUT must be a valid RUN."));
    }
}
